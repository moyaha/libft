#include <unistd.h>

char	*ft_strcat(char *s1, const char *s2)
{
	int m;
	int p;

	m = 0;
	while (s1[m])
		m++;
	p = 0;
	while (s2[p])
	{
		s1[m] = s2[p];
		m++;
		p++;
	}
	s1[m] = '\0';
	return (s1);
}
